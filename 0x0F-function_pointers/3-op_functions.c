#include "3-calc.h"
/**
 * op_add - Entry point
 * @a:input
 * @b:input
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Entry point
 * @a:input
 * @b: input
 * Return:substraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_div - Entry point
 * @a:input
 * @b:input
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Entry point
 * @a:input
 * @b:input
 * Return: reminder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
